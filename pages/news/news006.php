        <p class="center">ANALIZA SYTUACJI</p>
        <p class="pTextIndent">„Przepisy Gry” kwalifikują odepchnięcie przeciwnika jako naruszenie art. XII. Jest to gra niedozwolona – przewinienie karane rzutem wolnym lub karnym, w zależności od miejsca zdarzenia. Tylko tyle i jednocześnie – aż tyle… A zatem: to decyzja zgodna z literą „Przepisów Gry”, czy zgodna z ich duchem/ideą? Co oceniamy w tej sytuacji?<br>
          – rozmyślność przewinienia;<br>
          – intensywność przewinienia;<br>
          – jego następstwa i ich wpływ na przebieg gry;<br>
          – adekwatność kary w odniesieniu do skali przewinienia.<br>
        <p class="center">Analiza materiału filmowego nasuwa następujące wnioski:<br></p>
        <p>
          – zachowanie obrońcy było celowe. Mówimy o sytuacji, gdy zawodnik odpycha oburącz w plecy stojącego przed nim (widocznego dłuższy czas) przeciwnika;<br>
          – siła odepchnięcia nie jest łatwa do oceny. Nie była duża, ale wystarczająca, aby odepchnięty z tyłu zawodnik stracił równowagę i upadł;<br>
          – wpływ na przebieg gry: w chwili przewinienia (ostatnie ujęcia pokazują to dobitnie) obrońca nie mógł dokładnie oszacować czy napastnik będzie w stanie skutecznie ją dosięgnąć;<br>
          – rzut karny to najcięższa kara zespołowa. Przewinienia obrońców nie powinny budzić wątpliwości. Trudno tutaj mówić o przypadku. Mamy do czynienia z brakiem odpowiedzialności ze strony obrońcy. Jeżeli przyjmiemy, że od strony taktycznej (wpływ na dalszy przebieg gry) nie miało ono znaczenia, należy je ocenić jako bezmyślne… Czy jest jakiś powód, aby je usprawiedliwiać?<br>
        </p>
        <p class="pTextIndent">
        <h4>Metodologia i ostateczna decyzja:</h4>
        <p class="pTextIndent"> Sytuacja nie jest czarno-biała. Jednak biorąc pod uwagę powyższe elementy, zdecydowanie więcej argumentów przemawia za boiskową decyzją sędziego. Jest ona słuszna. Śląskowi Wrocław należał się rzut karny.
        </p>
        </p>
        <h3>#20 kolejka. Stal Mielec - Raków Częstochowa
        </h3>
        <p class="pTextIndent">
          Zawodnik Rakowa dośrodkowuje piłkę w pole karne Stali. Futbolówka zmierza w okolice pola bramkowego, a w jej kierunku podąża napastnik gości. Mimo ewidentnej próby, nie zagrywa on piłki. Nie zagrywa jej również bramkarz Stali, który cały czas jest w okolicach linii bramkowej. Piłka nie zmienia trajektorii lotu. Po odbiciu od murawy, tuż przed bramkarzem, wpada do bramki.
        </p>
