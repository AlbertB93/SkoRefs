      <div class="news--simple--container--box006">
        <p class="pTextIndent">Po każdej kolejce, na stronie PZPN możemy znaleźć artykuł pod nazwą "klip tygodnia". W artykule przedstawione jest wyjaśnienie decyzji sędziego w jednej lub kilku sytuacji z wybranego meczu danej kolejki. Nadrabiamy zaległości na stronie skarżyskich gwizdków, poniżej kilka ciekawych sytuacji z trzech pierwszych kolejek rundy wiosennej sezonu 2022 / 2023 PKO BP Ekstraklasy.</p>
        <h3>#18 kolejka. Widzew Łódź - Pogoń Szczecin
        </h3>
        <p class="center"> Zdarzenie pierwsze: 49. minuta meczu <br></p>
        <p class="pTextIndent">
          W pole karne Pogoni dynamiczne wbiega zawodnik Widzewa nr 92. Wygrywa on walkę o pozycję, ponieważ jest bliżej piłki niż atakujący go obrońca. W pewnym momencie zawodnik Widzewa wykonuje półobrót i ma wystawioną w kierunku bramki prawą nogę. Wówczas dochodzi do kontaktu/starcia z nadbiegającym obrońcą. Napastnik upada, krzyczy, grymas wykrzywia jego twarz, on sam zwija się z bólu. Tymczasem sędzia (ustawiony optymalnie) wymownym gestem pokazuje: „nie ma mowy o przewinieniu – gramy dalej”. Czy słusznie?
        </p>
        <div class="video--box">
          <iframe width="560" height="315" src="https://www.youtube.com/embed/Oh7tFIzN7gI" title="YouTube video player" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture; web-share" allowfullscreen></iframe>
        </div>
        <p class="pTextIndent">
          Analizując zdarzenie należy zwrócić uwagę na kilka elementów (pomijamy faul w ataku w wykonaniu zawodnika nr 92 chwilę wcześniej, bo wtedy dyskusja byłaby bezprzedmiotowa):<br>
          1. Napastnik wygrał pozycję, był bliżej piłki. Należy podkreślić, że taka pozycja uprzywilejowuje go w sensie taktycznym, ale nie daje mu szczególnych, dodatkowych uprawnień w rozumieniu art. XII „Przepisów Gry”.<br>
          2. Obrońca, walcząc o piłkę i o pozycję, zmuszony jest swój atak przeprowadzić dokładnie, uważnie oraz starannie.<br>
          3. Kontakt nie jest równoważny z przewinieniem, grą niedozwoloną, czyli faulem. To „part of the football”. Nie możemy tego zabraniać ani eliminować. Stracilibyśmy coś bardzo ważnego. Solą piłki nożnej są nie tylko bramki, ale także walka.<br>
        </p>
        <p class="pTextIndent">
        <h4>Metodologia i ostateczna decyzja:</h4>
        <p class="pTextIndent"> W tym przypadku wystawienie prawej nogi przez zawodnika nr 92 nie miało na celu:<br>
          – przygotowania lepszej pozycji do strzału;<br>
          – taktycznego osłonięcia piłki (co jest pracą i zadaniem napastnika), aby obrońcy trudniej było ją wybić.<br>
          W tym przypadku chodziło o zainicjowanie/sprowokowanie kontaktu z nadbiegającym obrońcą. To właśnie decyduje o tym, że podyktowanie rzutu karnego byłoby błędem.<br>
        </p>
        </p>
        <p class="center"> Zdarzenie drugie: doliczony czas gry – 90+5. minuta meczu<br></p>
        <p class="pTextIndent">
          Ponownie pole karne Pogoni. Strzał na bramkę. Piłka trafia w twarz, a następnie (prawie jednocześnie) w wysoko uniesioną lewą rękę obrońcy Pogoni. Doskonale (ponownie) ustawiony arbiter, błyskawicznie podejmuje decyzję: rzut karny dla Widzewa i żółta kartka dla obrońcy. Czy słusznie?
        </p>
        <div class="video--box">
          <iframe width="560" height="315" src="https://www.youtube.com/embed/AZfIL87Ft3o" title="YouTube video player" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture; web-share" allowfullscreen></iframe>
        </div>
        <p class="pTextIndent">
          Należy odpowiedzieć na podstawowe pytania:<br>
          1. Czy obrońca usiłował zagrać piłkę? Czy próbował ją odbić/zablokować, a piłka zmierzała w kierunku bramki?<br>
          2. Czy ręka ułożona była naturalnie? Czy była w obrysie ciała?<br>
          3. Czy ma znaczenie z jakiej odległości zagrana była piłka?<br>
          4. Czy kontakt piłki z twarzą obrońcy zmienia sposób oceny sytuacji?<br>
          Jeżeli mamy do czynienia z silnym strzałem z niewielkiej odległości i jeżeli nawet uznamy, że obrońca nie blokował piłki, to sam fakt, że ręka jest ułożona nienaturalnie (wysoko ponad linią barku) decyduje o przewinieniu.<br>
        <p class="center">Ważne jest to, że nie ma tutaj wcześniej rozmyślnego zagrania głową przez obrońcę. To był jedynie rykoszet.</p>
        </p>
        <p class="pTextIndent">
        <h4>Ostateczna decyzja:</h4>
        <p class="pTextIndent center"> Podyktowanie rzutu karnego i ukaranie obrońcy żółtą kartką (piłka zmierzała w kierunku bramki) było absolutnie prawidłowe.<br><br></p>
        <p class="pTextIndent">
          Dlaczego sędzia podbiegł do monitora i nie zmienił decyzji? Arbiter pojawił się przy monitorze z własnej inicjatywy, na co pozwala protokół VAR. Chciał jednoznacznie uświadomić wszystkim, że w tak istotnej i kluczowej sytuacji dopełnia wszelkiej staranności podejmując tę decyzję. Ale to już jest wyższa szkoła jazdy przy meczu o takim ciężarze gatunkowym i przy takiej atmosferze. Do tematu będziemy wracać przy kolejnych analizach.
        </p>
        </p>
        <h3>#19 kolejka. Pogoń Szczecin - Śląsk Wrocław
        </h3>
        <p class="pTextIndent">
          Dośrodkowanie w pole karne Pogoni. W momencie, gdy szybująca wysoko piłka zmierzała do zawodników, obrońca Pogoni nr 20 odepchnął oburącz w plecy napastnika Śląska nr 7. Zawodnik gości upadł, a arbiter podyktował rzut karny. VAR nie interweniował, decyzja z boiska została podtrzymana. Czy słusznie?
        </p>
        <div class="video--box">
          <iframe width="560" height="315" src="https://www.youtube.com/embed/PFV82UYlZ5E" title="YouTube video player" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture; web-share" allowfullscreen></iframe>
