        </div>
        <p class="center">METODOLOGIA OCENY</p>
        <p class="pTextIndent">
          W pierwszej kolejności sędziowie musieli ocenić, czy napastnik Rakowa, poruszający się w kierunku bramki, był na pozycji spalonej. Jak widać na powtórce, trzech atakujących graczy było na pozycji spalonej (przypominamy, że pozycja spalona to jeszcze nie spalony): nr 24, nr 21 i nr 4 – czyli zawodnik, który podjął ewidentną próbę zagrania piłki.<br><br>
          W kolejnej fazie należało zinterpretować czy doszło do naruszenia „Przepisów Gry” w zakresie spalonego. Poniżej przedstawiam zapis z książki:<br><br>
          „Zawodnik, który przebywa na pozycji spalonej w momencie, gdy piłka zostaje zagrana […] przez współpartnera, może być ukarany jedynie wtedy, gdy jest aktywny w grze poprzez:<br><br>
          – przeszkadzanie przeciwnikowi przez […] ewidentną próbę zagrania piłki, która znajduje się blisko, gdy próba ta ma wpływ na przeciwnika […].”<br><br>
          Zawodnik nr 4 był aktywny w grze. Jego zachowanie ocenia się jako ewidentną próbę zagrania piłki. Była ona blisko tego napastnika, a dodatkowo działanie podjęte przez niego miało wpływ na zachowanie przeciwnika (bramkarza). Z tego powodu nie wykonał on parady bramkarskiej. Używając potocznego języka, zawodnik Rakowa nr 4, swoją ewidentną próbą zagrania piłki, zmylił bramkarza, który chciał interweniować. W efekcie piłka wpadła do bramki.
        <p class="center">ANALIZA VAR<br></p>
        <p>
          VAR zarekomendował sędziemu wideoweryfikację (tzw. OFR – on field review). Dlaczego, skoro sytuacja dotyczyła spalonego?<br><br>
          Ponieważ to zdarzenie wymagało interpretacji „Przepisów Gry”. Gdyby zawodnik nr 4 zdobył bramkę, zagrywając piłkę, to decyzja byłaby podjęta na podstawie informacji od VAR (tzw. VAR – only review), czyli bez OFR.
